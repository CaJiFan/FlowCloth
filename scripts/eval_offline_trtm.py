LATEX_TEMPLATE = r"""
% ─────────────────────────────────────────────────────────────────────────────
% Auto-generated by eval_offline_trtm.py (Aggregated N={n_seeds} seeds)
% ─────────────────────────────────────────────────────────────────────────────
\begin{{table*}}[t]
  \centering
  \caption{{Cloth state estimation on TRTM validation set. Results are reported as Mean $\pm$ Standard Deviation across {n_seeds} seeds.
            Best results per column in \textbf{{bold}}. Evaluated on {n_samples} samples.}}
  \label{{tab:state_est_trtm}}
  \setlength{{\tabcolsep}}{{3pt}}
  \begin{{tabular}}{{l c c c c c c c c c}}
    \toprule
    \textbf{{Method}} &
    \textbf{{$\lambda_{{sw}}$}} & % <-- NEW COLUMN
    \textbf{{MSE\,↓}} &
    \textbf{{CD-L1\,↓}} &
    \textbf{{F@1cm\,↑}} &
    \textbf{{F@2cm\,↑}} &
    \textbf{{Corner\,↓}} &
    \textbf{{Hausdorff\,↓}} &
    \textbf{{Perim.\,↓}} &
    \textbf{{Lat.\,(ms)\,↓}} \\
    \midrule
{rows}    \bottomrule
  \end{{tabular}}
\end{{table*}}
"""

def build_latex_aggregated(agg_results: list) -> str:
    bests = {}
    for key, better in zip(COL_KEYS, COL_BETTER):
        vals = [r[key + "_mean"] for r in agg_results]
        bests[key] = min(vals) if better == "min" else max(vals)

    rows = ""
    for i, r in enumerate(agg_results):
        cells = []
        for k, b in zip(COL_KEYS, COL_BETTER):
            mean_val = r[k + "_mean"]
            std_val = r[k + "_std"]
            is_best = math.isclose(mean_val, bests[k], rel_tol=1e-5)
            
            dec = 3 if "f_score" in k else 4
            
            if is_best:
                cells.append(f"$\\mathbf{{{mean_val:.{dec}f} \\pm {std_val:.{dec}f}}}$")
            else:
                cells.append(f"${mean_val:.{dec}f} \\pm {std_val:.{dec}f}$")
                
        row_str = " & ".join(cells)
        
        sep = r"    \midrule" + "\n" if i > 0 and r['label'] != agg_results[i-1]['label'] else ""
        step_lbl = f"({r['inference_steps']} steps)"
        
        # Inject the weight_tex right after the method name
        rows += f"{sep}    {r['label']} {step_lbl} & {r['weight_tex']} & {row_str} \\\\\n"

    n_samples = agg_results[0]["num_val_samples"]
    n_seeds = len(BASE_EVAL_RUNS[0]["seeds"])
    return LATEX_TEMPLATE.format(rows=rows, n_samples=n_samples, n_seeds=n_seeds)


# ─────────────────────────────────────────────────────────────────────────────
# Efficiency table (Aggregated)
# ─────────────────────────────────────────────────────────────────────────────

_METHOD_MACRO = {
    (False, False): r"\fullddpm",
    (True,  False): r"\edgeddpm",
    (False, True):  r"\fullfm",
    (True,  True):  r"\edgefm",
}
_NODE_MACRO = {
    False: r"$\Nfull$ (full)",
    True:  r"$\Nedge$ (edge)",
}
_EFF_METHOD_ORDER = [(False, False), (True, False), (False, True), (True, True)]
_EFF_BOLD_KEY     = (True, True)

def build_efficiency_latex(agg_results: list) -> str:
    # Uses the already aggregated latency and fps means
    sorted_results = sorted(
        agg_results,
        key=lambda r: (
            _EFF_METHOD_ORDER.index((r["is_edge"], r["use_fm"])) if (r["is_edge"], r["use_fm"]) in _EFF_METHOD_ORDER else 99,
            r["inference_steps"]
        )
    )

    rows = ""
    prev_method = None
    for r in sorted_results:
        is_edge = r["is_edge"]
        use_fm = r["use_fm"]
        inf_steps = r["inference_steps"]
        
        macro   = _METHOD_MACRO.get((is_edge, use_fm), r"\unknown")
        nodes   = _NODE_MACRO[is_edge]
        steps_s = f"{inf_steps} (Euler)" if use_fm else str(inf_steps)
        lat_ms  = r["latency_ms_mean"]
        fps_val = r["fps_mean"]

        method_key = (is_edge, use_fm)
        if prev_method is not None and method_key != prev_method:
            rows += "        \\midrule\n"
        prev_method = method_key

        is_bold = (method_key == _EFF_BOLD_KEY)
        if is_bold:
            row = (
                f"        \\textbf{{{macro}}}  & "
                f"$\\mathbf{{\\Nedge}}$ \\textbf{{(edge)}} & "
                f"\\textbf{{{steps_s}}} & "
                f"\\textbf{{{lat_ms:.1f}}} & "
                f"\\textbf{{{fps_val:.1f}}} \\\\\n"
            )
        else:
            row = (
                f"        {macro:<17s} & {nodes:<24s} & {steps_s:<11s} "
                f"& {lat_ms:.1f}    & {fps_val:.1f}    \\\\\n"
            )
        rows += row

    return (
        "% Auto-generated by eval_offline_trtm.py\n"
        "\\begin{table}[h]\n"
        "    \\centering\n"
        "    \\caption{%\n"
        "        Inference efficiency on TRTM dataset, single NVIDIA L40S GPU.\n"
        "        The ``Steps'' column refers to denoising steps (DDPM) or Euler integration steps (FM).\n"
        "    }\n"
        "    \\label{tab:efficiency_trtm}\n"
        "    \\begin{tabular}{llccc}\n"
        "        \\toprule\n"
        "        \\textbf{Method} & \\textbf{Nodes} & \\textbf{Steps} "
        "& \\textbf{Time (ms) $\\downarrow$} & \\textbf{FPS $\\uparrow$} \\\\\n"
        "        \\midrule\n"
        + rows
        + "        \\bottomrule\n    \\end{tabular}\n\\end{table}\n"
    )

